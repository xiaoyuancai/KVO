#import "XYCPerson.h"

@implementation XYCPerson

-(NSString *)fullName{
    return [NSString stringWithFormat:@"%@%@",_lastName,_firstName];
}

//关联监听时使用
+(NSSet<NSString *> *)keyPathsForValuesAffectingFullName{
    return [NSSet setWithObjects:@"lastName",@"firstName",nil];
}

@end
